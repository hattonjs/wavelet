const getInstrumentKey = (name: string) =>
  name
    .toLocaleLowerCase()
    .replaceAll(/[^a-z0-9_ ]/gm, "")
    .replaceAll(/\s+/gm, "_")

export const getInstrumentKeys = () => GM_PATCH_NAMES.map(getInstrumentKey)
